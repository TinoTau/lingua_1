CJK_RE = re.compile(r"[\u4e00-\u9fff]")
FORBIDDEN_SUBSTR = frozenset("请吗确认预订打包菜单堂食点餐外卖发票排号")
FORBIDDEN_CHARS = re.compile(r"[请吗]")
FORBIDDEN_COMPOUNDS = re.compile(r"确认|预订|打包|菜单|堂食|点餐|外卖|发票|排号")
PHRASE_SUFFIX = re.compile(r"(服务|确认)$")
GENERIC_BLOCK = frozenset({"服务", "工作", "发布", "模型", "优化", "下文"})


def _block(text: str) -> list[str]:
    return [w.strip() for w in text.split() if w.strip()]


def _compound(prefixes: list[str], suffixes: list[str]) -> list[str]:
    out: list[str] = []
    for p in prefixes:
        for s in suffixes:
            term = p + s
            if 2 <= len(term) <= 5:
                out.append(term)
    return out


def cjk_count(word: str) -> int:
    return len(CJK_RE.findall(word))


def validate_term(word: str) -> bool:
    w = word.strip()
    if not w or w in GENERIC_BLOCK:
        return False
    if w.endswith("的") or w.endswith("了"):
        return False
    if FORBIDDEN_CHARS.search(w) or FORBIDDEN_COMPOUNDS.search(w):
        return False
    if any(ch in w for ch in FORBIDDEN_SUBSTR):
        return False
    if len(w) >= 3 and PHRASE_SUFFIX.search(w):
        return False
    n = cjk_count(w)
    return n == len(w) and 2 <= n <= 5


def _filter_terms(terms: list[str]) -> list[str]:
    seen: set[str] = set()
    out: list[str] = []
    for t in terms:
        if not validate_term(t) or t in seen:
            continue
        seen.add(t)
        out.append(t)
    return out


